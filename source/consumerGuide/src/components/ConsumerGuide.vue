  name: "ConsumerGuide",
  data() {
    return {
      hasGroup: false,
      showDailog: false
    };
  },
  methods: {
    bottomAction: function() {
      //有群组直接去购买电池套餐页面
      if (this.hasGroup) {
        window.location.href = "../buyingBatteries/index.html";
      }
      //无群组去预约
      else {
        let currentUrl = window.location.href;
        var param = "";
        if (currentUrl.indexOf("?") != -1) {
          param = currentUrl.substr(currentUrl.indexOf("?"), currentUrl.length);
        }
        if (param && param.length > 0) {
          param += "&appointment=1";
        } else {
          param = "?appointment=1";
        }
        window.location.href = "../providersList/index.html" + param;
      }
    },
    confirmAction: function() {
      window.location.href = "IMMOTOR://backAction";
    },
    getUrlParam: function(name) {
      var reg = new RegExp("(^|&)" + name + "=([^&]*)(&|$)");
      var r = window.location.search.substr(1).match(reg);
      if (r != null) return unescape(decodeURIComponent(r[2]));
      return null;
    }
  },
  mounted() {
    var by_client = this.getUrlParam("by_client");
    if (by_client == "1") {
      this.hasGroup = true;
    }
    var show = this.getUrlParam("show");
    if (show == "1") {
      this.showDailog = true;
    }
  }
};
